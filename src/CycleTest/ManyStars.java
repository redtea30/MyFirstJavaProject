package CycleTest;

public class ManyStars {
    public static void main(String[] args) {
        //打印5排星星for
        int i, j;
        for (i = 1; i < 6; i++) {
            for (j = 1; j < 15; j++) {
                System.out.print('*');
            }
            System.out.println();
        }


    }
}
